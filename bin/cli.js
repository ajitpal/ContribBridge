// в”Ђв”Ђв”Ђ Command: connect в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
async function cmdConnect(repoArg) {
  if (!repoArg) {
    printError('Usage: contribbridge connect --repo <org/repo>');
    process.exit(1);
  }

  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!process.env.GITHUB_TOKEN) {
    printError('GITHUB_TOKEN not set. Run `contribbridge init` first.');
    process.exit(1);
  }

  // Dynamic import of github.js (it relies on env vars)
  const githubPath = pathToFileURL(path.join(ROOT, 'src', 'github.js')).href;
  const { getRepoVisibility, registerWebhook } = await import(githubPath);

  print(`\n${colors.cyan}Connecting to ${colors.bright}${repoArg}${colors.reset}...\n`);

  // Check repo visibility
  const { isPrivate, name } = await getRepoVisibility(repoArg);

  if (isPrivate) {
    print(`  Repository ${name} is PRIVATE.`);

    if (!process.env.CONTRIBBRIDGE_LICENSE_KEY) {
      printError(
        'Private repos require ContribBridge Pro.\n' +
        `  Upgrade at: https://contribbridge.dev/upgrade?repo=${encodeURIComponent(repoArg)}\n` +
        '  Once purchased, add CONTRIBBRIDGE_LICENSE_KEY to your .env file.'
      );
      process.exit(1);
    }

    printSuccess('License key found вЂ” private repo translation enabled.');
  } else {
    printSuccess(`Repository ${name} is PUBLIC вЂ” free translation enabled.`);
  }

  // Register webhook
  const webhookUrl = process.env.WEBHOOK_URL || `http://localhost:${process.env.PORT || 4000}`;
  await registerWebhook(repoArg, `${webhookUrl}/webhook/github`);
  printSuccess(`Webhook registered for ${repoArg}`);
  print('');
  print('  Next step:');
  print('    contribbridge watch');
  print('');
}

// в”Ђв”Ђв”Ђ Command: watch в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
async function cmdWatch() {
  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!existsSync(ENV_PATH)) {
    printError('.env file not found. Run `contribbridge init` first.');
    process.exit(1);
  }

  printBrand(BANNER);
  printStep('Starting ContribBridge translation server...\n');

  // Import and start the server
  const serverPath = pathToFileURL(path.join(ROOT, 'src', 'server.js')).href;
  const { startServer } = await import(serverPath);
  await startServer();
}

// в”Ђв”Ђв”Ђ Command: help в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
function cmdHelp() {
  printBrand(BANNER);
  print(`${colors.bright}Usage:${colors.reset} ${colors.cyan}contribbridge <command> [options]${colors.reset}\n`);
  print(`${colors.bright}Commands:${colors.reset}`);
  print(`  ${colors.cyan}init${colors.reset}                     Set up API keys and generate .env file`);
  print(`  ${colors.cyan}connect --repo <org/repo>${colors.reset} Register GitHub webhook on a repository`);
  print(`  ${colors.cyan}watch${colors.reset}                    Start the translation server + dashboard`);
  print(`  ${colors.cyan}help${colors.reset}                     Show this help message`);
  print('');
  print(`${colors.bright}Examples:${colors.reset}`);
  print('  npx contribbridge init');
  print('  npx contribbridge connect --repo myorg/myrepo');
  print('  npx contribbridge watch');
  print('');
}

// в”Ђв”Ђв”Ђ Router в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const args = process.argv.slice(2);
const command = args[0];

switch (command) {
  case 'init':
    cmdInit().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'connect': {
    // Parse --repo flag
    const repoIndex = args.indexOf('--repo');
    const repo = repoIndex !== -1 ? args[repoIndex + 1] : args[1];
    cmdConnect(repo).catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;
  }

  case 'watch':
    cmdWatch().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'help':
  case '--help':
  case '-h':
    cmdHelp();
    break;

  default:
    if (command) {
      printError(`Unknown command: ${command}`);
    }
    cmdHelp();
    break;
}
