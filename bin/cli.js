function print(msg) {
  console.log(msg);
}

function printSuccess(msg) {
  console.log(`✓ ${msg}`);
}

function printError(msg) {
  console.error(`✗ ${msg}`);
}

/** Prompt the user for a single line of input via stdin */
function ask(question) {
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim());
    });
  });
}

// ─── Command: init ───────────────────────────────────────────────
async function cmdInit() {
  print(BANNER);
  print('Welcome to ContribBridge! Let\'s set up your environment.\n');

  // Prompt for required API keys
  const lingoKey = await ask('  Lingo.dev API Key (from lingo.dev/dashboard → Settings → API Keys): ');
  if (!lingoKey) {
    printError('LINGODOTDEV_API_KEY is required. Get one at https://lingo.dev/dashboard');
    process.exit(1);
  }

  const ghToken = await ask('  GitHub Token (fine-grained PAT with Issues + Webhooks perms):       ');
  if (!ghToken) {
    printError('GITHUB_TOKEN is required. Create one at https://github.com/settings/tokens?type=beta');
    process.exit(1);
  }

  // Auto-generate webhook secret
  const webhookSecret = randomBytes(16).toString('hex'); // 32-char hex

  // Write .env file
  const envContent = [
    '# ContribBridge — Environment Variables',
    '# Generated by `contribbridge init`',
    '',
    '# ─── Required ─────────────────────────────────────────',
    `LINGODOTDEV_API_KEY=${lingoKey}`,
    `GITHUB_TOKEN=${ghToken}`,
    '',
    '# ─── Auto-generated ───────────────────────────────────',
    `GITHUB_WEBHOOK_SECRET=${webhookSecret}`,
    '',
    '# ─── Optional (safe defaults) ─────────────────────────',
    'PORT=4000',
    'TARGET_LOCALE=en',
    'CACHE_TTL=3600',
    'NODE_ENV=development',
    '',
    '# ─── Pro License (private repos only) ─────────────────',
    '# CONTRIBBRIDGE_LICENSE_KEY=',
    '',
    '# ─── Billing (billing server only) ────────────────────',
    '# STRIPE_SECRET_KEY=',
    '# STRIPE_WEBHOOK_SECRET=',
    '# STRIPE_PRICE_INDIE=',
    '# STRIPE_PRICE_TEAM=',
    '# RESEND_API_KEY=',
    '',
  ].join('\n');

  writeFileSync(ENV_PATH, envContent, 'utf-8');

  print('');
  printSuccess('.env file created');
  printSuccess(`Webhook Secret: ${webhookSecret}`);
  print('');
  print('  Next steps:');
  print('    1. contribbridge connect --repo <org/repo>');
  print('    2. contribbridge watch');
  print('');
  print('  Or start the server directly:');
  print('    node src/server.js');
  print('');
}

// ─── Command: connect ────────────────────────────────────────────
async function cmdConnect(repoArg) {
  if (!repoArg) {
    printError('Usage: contribbridge connect --repo <org/repo>');
    process.exit(1);
  }

  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!process.env.GITHUB_TOKEN) {
    printError('GITHUB_TOKEN not set. Run `contribbridge init` first.');
    process.exit(1);
  }

  // Dynamic import of github.js (it relies on env vars)
  const { getRepoVisibility, registerWebhook } = await import(
    path.join(ROOT, 'src', 'github.js')
  );

  print(`\nConnecting to ${repoArg}...\n`);

  // Check repo visibility
  const { isPrivate, name } = await getRepoVisibility(repoArg);

  if (isPrivate) {
    print(`  Repository ${name} is PRIVATE.`);

    if (!process.env.CONTRIBBRIDGE_LICENSE_KEY) {
      printError(
        'Private repos require ContribBridge Pro.\n' +
        `  Upgrade at: https://contribbridge.dev/upgrade?repo=${encodeURIComponent(repoArg)}\n` +
        '  Once purchased, add CONTRIBBRIDGE_LICENSE_KEY to your .env file.'
      );
      process.exit(1);
    }

    printSuccess('License key found — private repo translation enabled.');
  } else {
    printSuccess(`Repository ${name} is PUBLIC — free translation enabled.`);
  }

  // Register webhook
  const webhookUrl = process.env.WEBHOOK_URL || `http://localhost:${process.env.PORT || 4000}`;
  await registerWebhook(repoArg, `${webhookUrl}/webhook/github`);
  printSuccess(`Webhook registered for ${repoArg}`);
  print('');
  print('  Next step:');
  print('    contribbridge watch');
  print('');
}

// ─── Command: watch ──────────────────────────────────────────────
async function cmdWatch() {
  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!existsSync(ENV_PATH)) {
    printError('.env file not found. Run `contribbridge init` first.');
    process.exit(1);
  }

  print(BANNER);
  print('Starting ContribBridge server...\n');

  // Import and start the server
  const { startServer } = await import(path.join(ROOT, 'src', 'server.js'));
  await startServer();
}

// ─── Command: help ───────────────────────────────────────────────
function cmdHelp() {
  print(BANNER);
  print('Usage: contribbridge <command> [options]\n');
  print('Commands:');
  print('  init                     Set up API keys and generate .env file');
  print('  connect --repo <org/repo> Register GitHub webhook on a repository');
  print('  watch                    Start the translation server + dashboard');
  print('  help                     Show this help message');
  print('');
  print('Examples:');
  print('  npx contribbridge init');
  print('  npx contribbridge connect --repo myorg/myrepo');
  print('  npx contribbridge watch');
  print('');
}

// ─── Router ──────────────────────────────────────────────────────
const args = process.argv.slice(2);
const command = args[0];

switch (command) {
  case 'init':
    cmdInit().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'connect': {
    // Parse --repo flag
    const repoIndex = args.indexOf('--repo');
    const repo = repoIndex !== -1 ? args[repoIndex + 1] : args[1];
    cmdConnect(repo).catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;
  }

  case 'watch':
    cmdWatch().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'help':
  case '--help':
  case '-h':
    cmdHelp();
    break;

  default:
    if (command) {
      printError(`Unknown command: ${command}`);
    }
    cmdHelp();
    break;
}
