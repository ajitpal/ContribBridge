/** Prompt the user for a single line of input via stdin */
function ask(question) {
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim());
    });
  });
}

// ─── Command: init ───────────────────────────────────────────────
async function cmdInit() {
  printBrand(BANNER);
  printStep('Welcome to ContribBridge! Let\'s set up your environment.\n');

  // Prompt for required API keys
  const lingoKey = await ask(`${colors.bright}  Lingo.dev API Key:${colors.reset} (from lingo.dev/dashboard → Settings → API Keys): `);
  if (!lingoKey) {
    printError('LINGODOTDEV_API_KEY is required. Get one at https://lingo.dev/dashboard');
    process.exit(1);
  }

  const ghToken = await ask(`${colors.bright}  GitHub Token:${colors.reset}    (fine-grained PAT with Issues + Webhooks perms):       `);
  if (!ghToken) {
    printError('GITHUB_TOKEN is required. Create one at https://github.com/settings/tokens?type=beta');
    process.exit(1);
  }

  // Prompt for Smee.io tunnel (optional but recommended for local)
  printStep('Local Development: Do you want to use Smee.io for webhook tunneling?');
  const useSmee = (await ask('  Use Smee.io? (y/n): ')).toLowerCase() === 'y';
  let smeeUrl = '';
  if (useSmee) {
    smeeUrl = await ask(`  Smee URL:${colors.reset} (leave blank to generate a new one): `);
    if (!smeeUrl) {
      const uniqueId = randomBytes(4).toString('hex');
      smeeUrl = `https://smee.io/contribbridge-${uniqueId}`;
      print(`  Generated Smee URL: ${colors.cyan}${smeeUrl}${colors.reset}`);
    }
  }

  // Auto-generate webhook secret
  const webhookSecret = randomBytes(16).toString('hex'); // 32-char hex

  // Write .env file
  const envLines = [
    '# ContribBridge — Environment Variables',
    '# Generated by `contribbridge init`',
    '',
    '# ─── Required ─────────────────────────────────────────',
    `LINGODOTDEV_API_KEY=${lingoKey}`,
    `GITHUB_TOKEN=${ghToken}`,
    '',
    '# ─── Auto-generated ───────────────────────────────────',
    `GITHUB_WEBHOOK_SECRET=${webhookSecret}`,
    '',
  ];

  if (smeeUrl) {
    envLines.push('# ─── Smee.io Tunnel ──────────────────────────────────');
    envLines.push(`SMEE_URL=${smeeUrl}`);
    envLines.push(`WEBHOOK_URL=${smeeUrl}`);
    envLines.push('');
  }

  envLines.push(...[
    '# ─── Optional (safe defaults) ─────────────────────────',
    'PORT=4000',
    'TARGET_LOCALE=en',
    'CACHE_TTL=3600',
    'NODE_ENV=development',
    '',
    '# ─── Pro License (private repos only) ─────────────────',
    '# CONTRIBBRIDGE_LICENSE_KEY=',
    '',
  ]);

  const envContent = envLines.join('\n');

  writeFileSync(ENV_PATH, envContent, 'utf-8');

  print('');
  printSuccess('.env file created');
  printSuccess(`Webhook Secret: ${colors.yellow}${webhookSecret}${colors.reset}`);
  print('');
  print(`${colors.bright}  Next steps:${colors.reset}`);
  print(`    1. ${colors.cyan}npx contribbridge connect --repo <org/repo>${colors.reset}`);
  print(`    2. ${colors.cyan}npx contribbridge watch${colors.reset}`);
  print('');
}

// ─── Command: connect ────────────────────────────────────────────
async function cmdConnect(repoArg) {
  if (!repoArg) {
    printError('Usage: contribbridge connect --repo <org/repo>');
    process.exit(1);
  }

  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!process.env.GITHUB_TOKEN) {
    printError('GITHUB_TOKEN not set. Run `contribbridge init` first.');
    process.exit(1);
  }

  // Dynamic import of github.js (it relies on env vars)
  const githubPath = pathToFileURL(path.join(APP_ROOT, 'src', 'github.js')).href;
  const { getRepoVisibility, registerWebhook } = await import(githubPath);

  print(`\n${colors.cyan}Connecting to ${colors.bright}${repoArg}${colors.reset}...\n`);

  // Check repo visibility
  const { isPrivate, name } = await getRepoVisibility(repoArg);

  if (isPrivate) {
    print(`  Repository ${name} is PRIVATE.`);

    if (!process.env.CONTRIBBRIDGE_LICENSE_KEY) {
      printError(
        'Private repos require ContribBridge Pro.\n' +
        `  Upgrade at: https://ajitpal.github.io/ContribBridge/#upgrade?repo=${encodeURIComponent(repoArg)}\n` +
        '  Once purchased, add CONTRIBBRIDGE_LICENSE_KEY to your .env file.'
      );
      process.exit(1);
    }

    printSuccess('License key found — private repo translation enabled.');
  } else {
    printSuccess(`Repository ${name} is PUBLIC — free translation enabled.`);
  }

  // Register webhook
  const webhookUrl = process.env.WEBHOOK_URL || process.env.SMEE_URL || `http://localhost:${process.env.PORT || 4000}`;
  await registerWebhook(repoArg, `${webhookUrl}/webhook/github`);
  printSuccess(`Webhook registered for ${repoArg}`);
  if (process.env.SMEE_URL) {
    print(`  Payloads will be tunneled via: ${colors.cyan}${process.env.SMEE_URL}${colors.reset}`);
  }
  print('');
  print('  Next step:');
  print(`    ${colors.cyan}npx contribbridge watch${colors.reset}`);
  print('');
}

// ─── Command: watch ──────────────────────────────────────────────
async function cmdWatch() {
  // Load environment variables
  const dotenv = await import('dotenv');
  dotenv.config({ path: ENV_PATH });

  if (!existsSync(ENV_PATH)) {
    printError('.env file not found. Run `npx contribbridge init` first.');
    process.exit(1);
  }

  printBrand(BANNER);
  printStep('Starting ContribBridge translation server...\n');

  // Start Smee client if configured
  if (process.env.SMEE_URL) {
    const smee = new SmeeClient({
      source: process.env.SMEE_URL,
      target: `http://localhost:${process.env.PORT || 4000}/webhook/github`,
      logger: console
    });
    smee.start();
    printSuccess(`Smee tunnel started: ${colors.cyan}${process.env.SMEE_URL}${colors.reset} ➜ :${process.env.PORT || 4000}`);
  }

  // Import and start the server
  const serverPath = pathToFileURL(path.join(APP_ROOT, 'src', 'server.js')).href;
  const { startServer } = await import(serverPath);
  await startServer();
}

// ─── Command: help ───────────────────────────────────────────────
function cmdHelp() {
  printBrand(BANNER);
  print(`${colors.bright}Usage:${colors.reset} ${colors.cyan}npx contribbridge <command> [options]${colors.reset}\n`);
  print(`${colors.bright}Commands:${colors.reset}`);
  print(`  ${colors.cyan}init${colors.reset}                     Set up API keys and generate .env file`);
  print(`  ${colors.cyan}connect --repo <org/repo>${colors.reset} Register GitHub webhook on a repository`);
  print(`  ${colors.cyan}watch${colors.reset}                    Start the translation server + dashboard`);
  print(`  ${colors.cyan}help${colors.reset}                     Show this help message`);
  print('');
  print(`${colors.bright}Examples:${colors.reset}`);
  print('  npx contribbridge init');
  print('  npx contribbridge connect --repo myorg/myrepo');
  print('  npx contribbridge watch');
  print('');
}

// ─── Router ──────────────────────────────────────────────────────
const args = process.argv.slice(2);
const command = args[0];

switch (command) {
  case 'init':
    cmdInit().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'connect': {
    // Parse --repo flag
    const repoIndex = args.indexOf('--repo');
    const repo = repoIndex !== -1 ? args[repoIndex + 1] : args[1];
    cmdConnect(repo).catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;
  }

  case 'watch':
    cmdWatch().catch((err) => {
      printError(err.message);
      process.exit(1);
    });
    break;

  case 'help':
  case '--help':
  case '-h':
    cmdHelp();
    break;

  default:
    if (command) {
      printError(`Unknown command: ${command}`);
    }
    cmdHelp();
    break;
}
